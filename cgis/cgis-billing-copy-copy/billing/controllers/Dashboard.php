	public function filter(){
		if (!$this->session->userdata('LOGGED')){
			$this->index();
			return;
		}
		
		$selected = $this->input->post('vals');
		
		$countKodeStatus = count($selected);
		$tmpArr = '';
		$job = '';
		$remark = '';
		for ($i=0; $i < $countKodeStatus ; $i++) { 
			$tmpArr .= "'".$selected[$i]."',";
			$panjangChar = strlen($selected[$i]);
			if ($panjangChar != 3) {
				$remark .= "'".$selected[$i]."',";
